function y = Copy_of_nonlinear_equations_system_kin(x, n, arr)
    y = zeros(4 * size(arr, 1) - 1, 1);
    %  начальные условия по кинетической энергии
    y(1:3, 1) = [0.5*sum((A(0, x, n, 1) .* x((9*n+1):(10*n)) .* cos(x((12*n+1):(13*n))) + A1(0, x, n, 1) .* sin(x((12*n+1):(13*n)))).^2) - arr(1, 1);
