                 0.5*sum((A(0, x, n, 3) .* x((11*n+1):(12*n)) .* cos(x((14*n+1):(15*n))) + A1(0, x, n, 3) .* sin(x((14*n+1):(15*n)))).^2) - arr(1, 3)];

    for t_id = 0:(size(arr, 1) - 1)  %  кинетика
        t = t_id;
        y((1:3) + 3 * (t_id + 1), 1) = [...
            0.5 * sum(2 * A(t, x, n, 1) .* A1(t, x, n, 1) .* ((x((9*n+1):(10*n)) .* cos(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n)))).^2) ...
            - A(t, x, n, 1).^2 .* (x((9*n+1):(10*n)).^3) .* sin(2*(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n)))) ...
            + A1(t, x, n, 1).^2 .* x((9*n+1):(10*n)) .* sin(2*(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n)))) ...
            + A(t, x, n, 1) .* A2(t, x, n, 1) .* x((9*n+1):(10*n)) .* sin(2*(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n)))) ...
            + 2 * A(t, x, n, 1) .* A1(t, x, n, 1) .* (x((9*n+1):(10*n)).^2) .* cos(2*(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n)))) ...
            + 2 * A1(t, x, n, 1) .* A2(t, x, n, 1) .* (sin(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n))).^2) ...
            + A1(t, x, n, 1).^2 .* x((9*n+1):(10*n)) .* sin(2*(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n))))) ...
            - x(15*n+4) * 0.5 * sum((A(t, x, n, 2) .* x((10*n+1):(11*n)) .* cos(x((10*n+1):(11*n)) * t + x((13*n+1):(14*n))) + A1(t, x, n, 2) .* sin(x((10*n+1):(11*n)) * t + x((13*n+1):(14*n)))).^2) ...
            - x(15*n+3) * 0.5 * sum((A(t, x, n, 3) .* x((11*n+1):(12*n)) .* cos(x((11*n+1):(12*n)) * t + x((14*n+1):(15*n))) + A1(t, x, n, 3) .* sin(x((11*n+1):(12*n)) * t + x((14*n+1):(15*n)))).^2) ...
            + (x(15*n+1) + x(15*n+6)) * 0.5 * sum((A(t, x, n, 1) .* x((9*n+1):(10*n)) .* cos(x((9*n+1):(10*n)) * t + x((12*n+1):(13*n))) + A1(t, x, n, 1) .* sin(x((9*n+1):(10*n)) * t + x((12*n+1):(13*n)))).^2);

            0.5 * sum(2 * A(t, x, n, 2) .* A1(t, x, n, 2) .* ((x((10*n+1):(11*n)) .* cos(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n)))).^2) ...
            - A(t, x, n, 2).^2 .* (x((10*n+1):(11*n)).^3) .* sin(2*(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n)))) ...
            + A1(t, x, n, 2).^2 .* x((10*n+1):(11*n)) .* sin(2*(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n)))) ...
            + A(t, x, n, 2) .* A2(t, x, n, 2) .* x((10*n+1):(11*n)) .* sin(2*(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n)))) ...
            + 2 * A(t, x, n, 2) .* A1(t, x, n, 2) .* (x((10*n+1):(11*n)).^2) .* cos(2*(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n)))) ...
            + 2 * A1(t, x, n, 2) .* A2(t, x, n, 2) .* (sin(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n))).^2) ...
            + A1(t, x, n, 2).^2 .* x((10*n+1):(11*n)) .* sin(2*(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n))))) ...
            - x(15*n+1) * 0.5 * sum((A(t, x, n, 1) .* x((9*n+1):(10*n)) .* cos(x((9*n+1):(10*n)) * t + x((12*n+1):(13*n))) + A1(t, x, n, 1) .* sin(x((9*n+1):(10*n)) * t + x((12*n+1):(13*n)))).^2) ...
            - x(15*n+5) * 0.5 * sum((A(t, x, n, 3) .* x((11*n+1):(12*n)) .* cos(x((11*n+1):(12*n)) * t + x((14*n+1):(15*n))) + A1(t, x, n, 3) .* sin(x((11*n+1):(12*n)) * t + x((14*n+1):(15*n)))).^2) ...
            + (x(15*n+2) + x(15*n+4)) * 0.5 * sum((A(t, x, n, 2) .* x((10*n+1):(11*n)) .* cos(x((10*n+1):(11*n)) * t + x((13*n+1):(14*n))) + A1(t, x, n, 2) .* sin(x((10*n+1):(11*n)) * t + x((13*n+1):(14*n)))).^2);

            0.5 * sum(2 * A(t, x, n, 3) .* A1(t, x, n, 3) .* ((x((11*n+1):(12*n)) .* cos(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n)))).^2) ...
            - A(t, x, n, 3).^2 .* (x((11*n+1):(12*n)).^3) .* sin(2*(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n)))) ...
            + A1(t, x, n, 3).^2 .* x((11*n+1):(12*n)) .* sin(2*(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n)))) ...
            + A(t, x, n, 3) .* A2(t, x, n, 3) .* x((11*n+1):(12*n)) .* sin(2*(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n)))) ...
            + 2 * A(t, x, n, 3) .* A1(t, x, n, 3) .* (x((11*n+1):(12*n)).^2) .* cos(2*(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n)))) ...
            + 2 * A1(t, x, n, 3) .* A2(t, x, n, 3) .* (sin(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n))).^2) ...
            + A1(t, x, n, 3).^2 .* x((11*n+1):(12*n)) .* sin(2*(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n))))) ...
            - x(15*n+6) * 0.5 * sum((A(t, x, n, 1) .* x((9*n+1):(10*n)) .* cos(x((9*n+1):(10*n)) * t + x((12*n+1):(13*n))) + A1(t, x, n, 1) .* sin(x((9*n+1):(10*n)) * t + x((12*n+1):(13*n)))).^2) ...
            - x(15*n+2) * 0.5 * sum((A(t, x, n, 2) .* x((10*n+1):(11*n)) .* cos(x((10*n+1):(11*n)) * t + x((13*n+1):(14*n))) + A1(t, x, n, 2) .* sin(x((10*n+1):(11*n)) * t + x((13*n+1):(14*n)))).^2) ...
            + (x(15*n+3) + x(15*n+5)) * 0.5 * sum((A(t, x, n, 3) .* x((11*n+1):(12*n)) .* cos(x((11*n+1):(12*n)) * t + x((14*n+1):(15*n))) + A1(t, x, n, 3) .* sin(x((11*n+1):(12*n)) * t + x((14*n+1):(15*n)))).^2)];
    end

    for t_id = 0:(size(arr, 1) - 2)  %    закон сохранения механической энергии
        t = t_id;
        y(3 * size(arr, 1) + t_id + 1, 1) = 0.5 * (...
            sum((A(t, x, n, 1) .* x((9*n+1):(10*n))).^2 ...
            + A(t, x, n, 1) .* A1(t, x, n, 1) .* x((9*n+1):(10*n)) .* sin(2*(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n)))) ...
            + (A1(t, x, n, 1) .* sin(x((9*n+1):(10*n)) .* t + x((12*n+1):(13*n)))).^2) ...
            ...
            + sum((A(t, x, n, 2) .* x((10*n+1):(11*n))).^2 ...
            + A(t, x, n, 2) .* A1(t, x, n, 2) .* x((10*n+1):(11*n)) .* sin(2*(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n)))) ...
            + (A1(t, x, n, 2) .* sin(x((10*n+1):(11*n)) .* t + x((13*n+1):(14*n)))).^2) ...
            ...
            + sum((A(t, x, n, 3) .* x((11*n+1):(12*n))).^2 ...
            + A(t, x, n, 3) .* A1(t, x, n, 3) .* x((11*n+1):(12*n)) .* sin(2*(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n)))) ...
            + (A1(t, x, n, 3) .* sin(x((11*n+1):(12*n)) .* t + x((14*n+1):(15*n)))).^2) ...
            ...
            - sum((A(0, x, n, 1) .* x((9*n+1):(10*n))).^2 ...
            + A(0, x, n, 1) .* A1(0, x, n, 1) .* x((9*n+1):(10*n)) .* sin(2*x((12*n+1):(13*n))) ...
            + (A1(0, x, n, 1) .* sin(x((12*n+1):(13*n)))).^2) ...
            ...
            - sum((A(0, x, n, 2) .* x((10*n+1):(11*n))).^2 ...
            + A(0, x, n, 2) .* A1(0, x, n, 2) .* x((10*n+1):(11*n)) .* sin(2*x((13*n+1):(14*n))) ...
            + (A1(0, x, n, 2) .* sin(x((13*n+1):(14*n)))).^2) ...
            ...
            - sum((A(0, x, n, 3) .* x((11*n+1):(12*n))).^2 ...
            + A(0, x, n, 3) .* A1(0, x, n, 3) .* x((11*n+1):(12*n)) .* sin(2*x((14*n+1):(15*n))) ...
            + (A1(0, x, n, 3) .* sin(x((14*n+1):(15*n)))).^2));
    end
end