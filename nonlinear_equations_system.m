%  Вычисляет невязку системы уравнений

function y = nonlinear_equations_system(x, n, arr, darr_dt)
    y = zeros(3 + 3*size(arr, 1) + 3*size(darr_dt, 1), 1);
    %  начальные условия по кинетической энергии
    y(1:3, 1) = [sum((x(1:3:(3*n-2)) + x(3:3:(3*n))) .* (x((9*n+1):(10*n)) .* cos(x((12*n+1):(13*n)))).^2) - arr(1, 1);
           sum((x(3*n+1:3:(6*n-2)) + x(3*n+3:3:(6*n))) .* (x((10*n+1):(11*n)) .* cos(x((13*n+1):(14*n)))).^2) - arr(1, 2);
           sum((x(6*n+1:3:(9*n-2)) + x(6*n+3:3:(9*n))) .* (x((11*n+1):(12*n)) .* cos(x((14*n+1):(15*n)))).^2) - arr(1, 3)];

    for t_id = 0:(size(arr, 1) - 1)  %  кинетика
        t = t_id;
        y((1:3) + 3 * (t_id + 1), 1) = [...
            sum(x(1:3:(3*n-2)) .* x(2:3:(3*n-1)) .* exp(-x(2:3:(3*n-1))*t) .* x((9*n+1):(10*n)).^2) ...
            + x(15*n+4) * sum((x(3*n+1:3:(6*n-2)) .* exp(-x(3*n+2:3:(6*n-1))*t) + x(3*n+3:3:(6*n))) .* x((10*n+1):(11*n)).^2) ...
            + x(15*n+3) * sum((x(6*n+1:3:(9*n-2)) .* exp(-x(6*n+2:3:(9*n-1))*t) + x(6*n+3:3:(9*n))) .* x((11*n+1):(12*n)).^2) ...
            - (x(15*n+1) + x(15*n+6)) * sum((x(1:3:(3*n-2)) .* exp(-x(2:3:(3*n-1))*t) + x(3:3:(3*n))) .* x((9*n+1):(10*n)).^2);
            sum(x(3*n+1:3:(6*n-2)) .* x(3*n+2:3:(6*n-1)) .* exp(-x(3*n+2:3:(6*n-1))*t) .* x((10*n+1):(11*n)).^2) ...
            + x(15*n+1) * sum((x(1:3:(3*n-2)) .* exp(-x(2:3:(3*n-1))*t) + x(3:3:(3*n))) .* x((9*n+1):(10*n)).^2) ...
            + x(15*n+5) * sum((x(6*n+1:3:(9*n-2)) .* exp(-x(6*n+2:3:(9*n-1))*t) + x(6*n+3:3:(9*n))) .* x((11*n+1):(12*n)).^2) ...
            - (x(15*n+2) + x(15*n+4)) * sum((x(3*n+1:3:(6*n-2)) .* exp(-x(3*n+2:3:(6*n-1))*t) + x(3*n+3:3:(6*n))) .* x((10*n+1):(11*n)).^2);
            sum(x(6*n+1:3:(9*n-2)) .* x(6*n+2:3:(9*n-1)) .* exp(-x(6*n+2:3:(9*n-1))*t) .* (x((11*n+1):(12*n)).^2)) ...
            + x(15*n+6) * sum((x(1:3:(3*n-2)) .* exp(-x(2:3:(3*n-1))*t) + x(3:3:(3*n))) .* x((9*n+1):(10*n)).^2) ...
            + x(15*n+2) * sum((x(3*n+1:3:(6*n-2)) .* exp(-x(3*n+2:3:(6*n-1))*t) + x(3*n+3:3:(6*n))) .* x((10*n+1):(11*n)).^2) ...
            - (x(15*n+3) + x(15*n+5)) * sum((x(6*n+1:3:(9*n-2)) .* exp(-x(6*n+2:3:(9*n-1))*t) + x(6*n+3:3:(9*n))) .* x((11*n+1):(12*n)).^2)];
    end
    
    for t_id = 0:(size(darr_dt, 1) - 1)
        t = t_id;
        y((1:3) + 3 * size(arr, 1) + 3 * (t_id+1), 1) = [...
            sum(x(1:3:(3*n-2)) .* x(2:3:(3*n-1)) .* exp(-x(2:3:(3*n-1))*t) .* (x((9*n+1):(10*n)).^2) .* (cos(x((9*n+1):(10*n))*t + x(12*n+1:13*n)).^2)) ...
            + sum((x(1:3:(3*n-2)) .* exp(-x(2:3:(3*n-1))*t) + x(3:3:(3*n))) .* (x((9*n+1):(10*n)).^3) .* sin(2*(x((9*n+1):(10*n))*t + x(12*n+1:13*n)))) ...
            + darr_dt(t_id+1, 1);
            sum(x(3*n+1:3:(6*n-2)) .* x(3*n+2:3:(6*n-1)) .* exp(-x(3*n+2:3:(6*n-1))*t) .* (x((10*n+1):(11*n)).^2) .* (cos(x((10*n+1):(11*n))*t + x(13*n+1:14*n)).^2)) ...
            + sum((x(3*n+1:3:(6*n-2)) .* exp(-x(3*n+2:3:(6*n-1))*t) + x(3*n+3:3:(6*n))) .* (x((10*n+1):(11*n)).^3) .* sin(2*(x((10*n+1):(11*n))*t + x(13*n+1:14*n)))) ...
            + darr_dt(t_id+1, 2);
            sum(x(6*n+1:3:(9*n-2)) .* x(6*n+2:3:(9*n-1)) .* exp(-x(6*n+2:3:(9*n-1))*t) .* (x((11*n+1):(12*n)).^2) .* (cos(x((11*n+1):(12*n))*t + x(14*n+1:15*n)).^2)) ...
            + sum((x(6*n+1:3:(9*n-2)) .* exp(-x(6*n+2:3:(9*n-1))*t) + x(6*n+3:3:(9*n))) .* (x((11*n+1):(12*n)).^3) .* sin(2*(x((11*n+1):(12*n))*t + x(14*n+1:15*n)))) ...
            + darr_dt(t_id+1, 3)];
    end
end

