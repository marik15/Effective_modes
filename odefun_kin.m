dydt(2) = y(1) * y(2) * (k(1) * y(1) - k(2) * y(2)) + y(2) * y(3) * (k(6) * y(3) - k(5) * y(2));
dydt(3) = y(1) * y(3) * (k(3) * y(1) - k(4) * y(3)) + y(2) * y(3) * (k(5) * y(2) - k(6) * y(3));
